class Solution {
public:
    int uniquePathsWithObstacles(vector<vector<int>> &obstacleGrid) {
        auto row = obstacleGrid.size();
        if (row == 0) return 0;
        auto col = obstacleGrid[0].size();
        if (col == 0) return 0;
        // int will overflow in some cases.
        long wayCounts[101][101];
        for (auto i = 0; i < row; ++i) {
            for (auto j = 0; j < col; ++j) {
                if (obstacleGrid[i][j] == 1) {
                    wayCounts[i][j] = 0;
                } else if (i == 0 && j == 0) {
                    wayCounts[i][j] = 1;
                } else if (i == 0) {
                    wayCounts[i][j] = wayCounts[i][j - 1];
                } else if (j == 0) {
                    wayCounts[i][j] = wayCounts[i - 1][j];
                } else {
                    wayCounts[i][j] = wayCounts[i - 1][j] + wayCounts[i][j - 1];
                }
            }
        }
        return wayCounts[row - 1][col - 1];
    }
};

